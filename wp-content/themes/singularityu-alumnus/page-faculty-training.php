                        <div class="section_title left">
                            <h2>Prep</h2>
                            <h4>How to Prepare For SingularityU Faculty Training</h4>
                            <p>At the end of your training week at Singularity University, you’ll be presenting your own SingularityU session from our stage to an audience of new colleagues and community 	members. This capstone presentation is a key part of our new-speaker certification process, and you’ll be given ample support and feedback throughout the week as you weave your subject matter expertise, experience and case studies into a compelling signature session that aligns with our narrative and curriculum arc.</p>
<br>
<p>To facilitate this process, we ask that you <strong>prepare a brief content outline for your prospective session and share it us by July 18, 2016.</strong></p>
<ul>
	<li>Your content outline should list the key talking points, examples, case studies, and takeaways that you might include in your session.</li>
	<li>You do NOT need to prepare a script or detailed outline in advance. You’ll be working intensively with our speaker coach, core faculty, and staff to build the narrative and messaging around your content.</li>
	<li>Please email your content outline to SU’s <a href="&#106;&#101;&#102;&#102;&#114;&#101;&#121;&#46;&#114;&#111;&#103;&#101;&#114;&#115;&#64;&#115;&#105;&#110;&#103;&#117;&#108;&#97;&#114;&#105;&#116;&#121;&#117;&#46;&#111;&#114;&#103;
">Director of Faculty Development</a>.</li>
</ul>
<br>
<p>While you’ll no doubt draw upon your previous work and experience in developing your SU session during faculty training, we expect your SU talk to be new, unique to our programs and reflective of key elements of our brand. Specifically,
<ul>
	<li>an <strong>alignment with the SU mission, Global Grand Challenge-orientation, and abundance mindset</strong></li>
	<li>a <strong>focus on the transformative future</strong> (as opposed to the history) of the exponential technologies, tools, and applications we explore</li>
	<li>a solid range of <strong>real-world examples and case studies</strong> (illustrating both present and future applications)</li>
</ul>
<br>
<p>As a primer on our programs, curriculum and work in the world-at-large, we recommend the following non-required reading:</p>
<ul>
<li>The <a href="https://drive.google.com/file/d/0BxKxpk-RherYZWdoNnlFTmplZzQ/view?usp=sharing">SU Case for Engagement (2015)</a> lays out our mission/narrative and vision for global community and impact.</li>
<li>The <a href="https://drive.google.com/open?id=0BxKxpk-RherYYmZ1WklQcWowR00">SU Impact Report (2015)</a> describes our Global Grand Challenge-orientation and provides an assessment of our global impact to date.</li>
<li>The <a href="http://guide.singularityu.org/">Guide to SU Programs (2016)</a> is a client-facing document describing the range and features of our programming.</li>
</ul>
